// markCouponUsed — called from stripeController after payment_intent.succeeded
// Fire-and-forget: caller must .catch() this
// ---------------------------------------------------------------------------
export const markCouponUsed = async (
  couponId: string,
  redeemedPurchaseId: string,
): Promise<void> => {
  await prisma.coupon.update({
    where: { id: couponId },
    data: {
      status: 'USED',
      usedAt: new Date(),
      redeemedPurchaseId,
    },
  });
};
